export const getStrain = (slug: string) => strains.find((s) => s.slug === slug);

export function relatedStrains(s: Strain, n = 4): Strain[] {
  return strains
    .filter((x) => x.slug !== s.slug)
    .map((x) => ({
      x,
      score:
        x.effects.filter((e) => s.effects.includes(e)).length * 2 +
        x.terpenes.filter((t) => s.terpenes.includes(t)).length +
        (x.genetics === s.genetics ? 1 : 0),
    }))
    .sort((a, b) => b.score - a.score)
    .slice(0, n)
    .map(({ x }) => x);
}
